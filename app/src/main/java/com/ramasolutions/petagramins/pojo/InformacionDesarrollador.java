package com.ramasolutions.petagramins.pojo;

public class InformacionDesarrollador {

    private String nombre;
    private String lugarNacimiento;
    private int foto;
    private String nombrePrograma;
    private String fechaDesarrollo;
    private String version;
    private String sobreMi;

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getLugarNacimiento() {
        return lugarNacimiento;
    }

    public void setLugarNacimiento(String lugarNacimiento) {
        this.lugarNacimiento = lugarNacimiento;
    }

    public int getFoto() {
        return foto;
    }

    public void setFoto(int foto) {
        this.foto = foto;
    }

    public String getNombrePrograma() {
        return nombrePrograma;
    }

    public void setNombrePrograma(String nombrePrograma) {
        this.nombrePrograma = nombrePrograma;
    }

    public String getFechaDesarrollo() {
        return fechaDesarrollo;
    }

    public void setFechaDesarrollo(String fechaDesarrollo) {
        this.fechaDesarrollo = fechaDesarrollo;
    }

    public String getVersion() {
        return version;
    }

    public void setVersion(String version) {
        this.version = version;
    }

    public String getSobreMi() {
        return sobreMi;
    }

    public void setSobreMi(String sobreMi) {
        this.sobreMi = sobreMi;
    }
}
